// STEMMING FUNCTION
// ─────────────────────────────────────────────────────────────────────────────
function stemSpanish(word) {
  if (!word) return word;
  const w = word.toLowerCase().trim();

  // 1. Check irregular forms first
  if (esIrregulars[w]) return esIrregulars[w];

  // 2. Strip reflexive/pronominal clitic suffixes ONLY when they follow
  //    a verb form ending (infinitive, gerund, imperative, present).
  //    Strategy: normalise accent first, then look for clitic after a recognisable ending.
  //    e.g. levantarse→levantar, levantándose→levantando, diciéndoselo→diciendo
  let stem = w.normalize('NFD').replace(/\u0301([bdfghjklmnpqrstvwxyz]*(?:[aeiou])[bdfghjklmnpqrstvwxyz]*(?=(?:se|lo|la|me|te|le|nos|os|les|los|las)+$))/gi, '$1').normalize('NFC');

  // Only strip clitics when the remainder looks like a verb form.
  // Gerund+clitics: base ends in ando/iendo, then clitics follow
  const gerundCliticPattern = /^(.{3,}(?:ando|iendo))((?:se|lo|la|le|les|nos|me|te|los|las)+)$/;
  // Infinitive+clitics: base ends in ar/er/ir
  const infCliticPattern = /^(.{3,}[aei]r)((?:se|lo|la|le|les|nos|me|te|los|las)+)$/;
  // Imperative+clitics: ends in vowel before clitics
  const cliticPattern = /^(.{3,}[aeio])((?:se|lo|la|le|les|nos|me|te|los|las)+)$/;
  let cliticMatch = gerundCliticPattern.exec(stem) || infCliticPattern.exec(stem) || cliticPattern.exec(stem);
  if (cliticMatch) {
    stem = cliticMatch[1];
  }
  // Re-check irregulars after stripping clitic
  if (stem !== w && esIrregulars[stem]) return esIrregulars[stem];

  // 3. If already ends in -ar/-er/-ir, it's an infinitive
  if (/[aei]r$/.test(stem)) return stem;

  // 4. Try regular verb ending removal (longest match first)

  // Future tense: endings attach directly to the infinitive
  // Match the infinitive suffix + future ending as a unit
  const futEnds = [
    ['aremos','ar'],['aréis','ar'],['arán','ar'],['ará','ar'],['arás','ar'],['aré','ar'],
    ['eremos','er'],['eréis','er'],['erán','er'],['erá','er'],['erás','er'],['eré','er'],
    ['iremos','ir'],['iréis','ir'],['irán','ir'],['irá','ir'],['irás','ir'],['iré','ir'],
  ];
  for (const [e,inf] of futEnds) {
    if (stem.endsWith(e)) return stem.slice(0,-e.length)+inf;
  }

  // Conditional tense: endings attach directly to the infinitive
  const condEnds = [
    ['aríamos','ar'],['aríais','ar'],['arían','ar'],['aría','ar'],['arías','ar'],
    ['eríamos','er'],['eríais','er'],['erían','er'],['ería','er'],['erías','er'],
    ['iríamos','ir'],['iríais','ir'],['irían','ir'],['iría','ir'],['irías','ir'],
  ];
  for (const [e,inf] of condEnds) {
    if (stem.endsWith(e)) return stem.slice(0,-e.length)+inf;
  }

  // Imperfect subjunctive -ara/-iera/-ase/-iese families
  const impSubjAr = ['áramos','arais','aran','aras','ara','ásemos','aseis','asen','ases','ase'];
  const impSubjEr = ['iéramos','ierais','ieran','ieras','iera','iésemos','ieseis','iesen','ieses','iese'];
  for (const e of impSubjAr) { if (stem.endsWith(e)) return stem.slice(0,-e.length)+'ar'; }
  for (const e of impSubjEr) { if (stem.endsWith(e)) return stem.slice(0,-e.length)+'er'; }

  // Gerund
  if (stem.endsWith('ando')) return stem.slice(0,-4)+'ar';
  if (stem.endsWith('iendo')) return stem.slice(0,-5)+'er';
  if (stem.endsWith('yendo')) return stem.slice(0,-5)+'er'; // e.g. leyendo→leer

  // Past participle: -ado → -ar, -ido → -ir (not -er, since -er verbs use -ido too but
  // in practice returning -ir for -ido is fine; lookup handles both via dict)
  if (stem.endsWith('ado')) return stem.slice(0,-3)+'ar';
  if (stem.endsWith('ido')) return stem.slice(0,-3)+'ir';

  // Preterite -ar
  const pretAr = ['asteis','amos','aron','aste','ó','é'];
  for (const e of pretAr) {
    if (stem.endsWith(e)) {
      const base = stem.slice(0,-e.length);
      if (base.length > 1) return base+'ar';
    }
  }

  // Preterite -er/-ir
  const pretEr = ['isteis','ieron','iste','imos','ió','í'];
  for (const e of pretEr) {
    if (stem.endsWith(e)) {
      const base = stem.slice(0,-e.length);
      if (base.length > 1) return base+'er';
    }
  }

  // Imperfect -ar
  const impAr = ['ábamos','abais','aban','abas','aba'];
  for (const e of impAr) {
    if (stem.endsWith(e)) return stem.slice(0,-e.length)+'ar';
  }

  // Imperfect -er/-ir
  const impEr = ['íamos','íais','ían','ías','ía'];
  for (const e of impEr) {
    if (stem.endsWith(e)) return stem.slice(0,-e.length)+'er';
  }

  // Present indicative (ambiguous ending)
  const presEnds = [
    ['áis','ar'],['amos','ar'],['ais','ar'],
    ['éis','er'],['emos','er'],
    ['imos','ir'],
    ['an','ar'],['as','ar'],
    ['en','er'],['es','er'],
    ['o','ar'],['a','ar'],['e','er'],
  ];
  for (const [e,inf] of presEnds) {
    if (stem.endsWith(e)) {
      const base = stem.slice(0,-e.length);
      if (base.length > 1) return base+inf;
    }
  }

  // Imperative 2p plural -ad/-ed/-id
  if (stem.endsWith('ad') && stem.length > 3) return stem.slice(0,-2)+'ar';
  if (stem.endsWith('ed') && stem.length > 3) return stem.slice(0,-2)+'er';
  if (stem.endsWith('id') && stem.length > 3) return stem.slice(0,-2)+'ir';

  return stem;
}

