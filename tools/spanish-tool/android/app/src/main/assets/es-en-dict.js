// LOOKUP HELPER
// Tries: exact match → stemmed match → lowercased stem match
// ─────────────────────────────────────────────────────────────────────────────
function lookupSpanish(word) {
  if (!word) return null;
  const w = word.toLowerCase().trim();
  if (esDict[w]) return esDict[w];
  if (esIrregulars[w]) {
    const inf = esIrregulars[w];
    return esDict[inf] ? `[${inf}] ${esDict[inf]}` : inf;
  }
  const s = stemSpanish(w);
  if (esDict[s]) return esDict[s];
  // Try without accent on stem
  const noAccent = s.normalize('NFD').replace(/[\u0300-\u036f]/g,'');
  if (esDict[noAccent]) return esDict[noAccent];
  // -er/-ir ambiguity: try swapping the last two letters of the infinitive ending
  if (s.endsWith('ir')) {
    const erAlt = s.slice(0,-2)+'er';
    if (esDict[erAlt]) return esDict[erAlt];
  } else if (s.endsWith('er')) {
    const irAlt = s.slice(0,-2)+'ir';
    if (esDict[irAlt]) return esDict[irAlt];
  }
  return null;
}
